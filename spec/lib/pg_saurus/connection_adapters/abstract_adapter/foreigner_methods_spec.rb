require 'spec_helper'

describe PgSaurus::ConnectionAdapters::AbstractAdapter::ForeignerMethods do
  class AbstractAdapter
    include ::PgSaurus::ConnectionAdapters::AbstractAdapter::ForeignerMethods
  end

  let(:adapter_stub) { AbstractAdapter.new }

  it ".supports_foreign_keys?" do
    expect(adapter_stub.supports_foreign_keys?).to be false
  end

  it ".foreign_keys" do
    expect(adapter_stub.foreign_keys(nil)).to eq([])
  end
end
